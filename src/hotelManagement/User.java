    private void jButton1_add_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_add_ActionPerformed
         String name = txtname.getText();
         String userName = txtuname.getText();
         String password = txtpass.getText();
        try {  
            pst = con.prepareStatement("insert into user( name, username, password ) values(?,?,?) ");
            pst.setString(1, name);
            pst.setString(2, userName);
            pst.setString(3, password);
                
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "User added successfully");
            
            txtname.setText("");
            txtuname.setText("");
            txtpass.setText("");
            txtname.requestFocus();
            
             
            loadUser();
                    
        } catch (SQLException ex) {
            Logger.getLogger(Room.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1_add_ActionPerformed

