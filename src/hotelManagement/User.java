    private void jButton2_edit_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_edit_ActionPerformed
            String userID = txtuid.getText();
            String pass = txtpass.getText();
            String name = txtname.getText();
            String userName =  txtuname.getText();
            
        try {  
            pst = con.prepareStatement("update user set name = ?, username = ?, password = ? where uid = ? ");
            pst.setString(1, name);
            pst.setString(2, userName);
            pst.setString(3, pass);
            pst.setString(4, userID);    
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "User updated successfully");
            
            txtuid.setText("ID");
            txtname.setText("");
            txtuname.setText("");
            txtpass.setText("");
           
             
            loadUser();
            
            jButton1_add_.setEnabled(true);
                    
        } catch (SQLException ex) {
            Logger.getLogger(Room.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2_edit_ActionPerformed

